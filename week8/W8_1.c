#include <stdio.h>
int main()
{
    //ใส่วันที่ วัน เดือน ปี ให้แสดงค่าวันต่อมา
    //1 1 2020
    //2 1 2020
    int d, m, y;
    printf("Enter Date: ");
    scanf("%d %d %d", &d, &m, &y);
    int day;
    if (m == 1 || m == 3 || m == 5 || m == 7 || m == 8 || m == 10 || m == 12)//Kom
    {
        day = 31;
    }
    else if (m == 4 || m == 6 || m == 9 || m == 11)//Yon
    {
        day = 30;
    }
    else if (m == 2)//checkLeapyear
    {
        if (y % 400 == 0 || (y % 4 == 0 && y % 100 != 0))
        {
            day = 29;
        }
        else
        {
            day = 28;
        }
    }
    if (d == day)
    {
        if (m == 12)
        {
            printf("1 1 %d", y + 1);
        }
        else
        {
            printf("1 %d %d", m + 1, y);
        }
    }
    else
    {
        printf("%d %d %d", d + 1, m, y);
    }

    return 0;
}

